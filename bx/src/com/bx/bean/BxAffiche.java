package com.bx.bean;

import java.util.Date;

public class BxAffiche {
	private String afficheId;
	private String adminId;
	private String afficheTitle;
	private String afficheCont;
	private Date afficheDate;
	public String getAfficheId() {
		return afficheId;
	}
	public void setAfficheId(String afficheId) {
		this.afficheId = afficheId;
	}
	public String getAdminId() {
		return adminId;
	}
	public void setAdminId(String adminId) {
		this.adminId = adminId;
	}
	public String getAfficheTitle() {
		return afficheTitle;
	}
	public void setAfficheTitle(String afficheTitle) {
		this.afficheTitle = afficheTitle;
	}
	public String getAfficheCont() {
		return afficheCont;
	}
	public void setAfficheCont(String afficheCont) {
		this.afficheCont = afficheCont;
	}
	public Date getAfficheDate() {
		return afficheDate;
	}
	public void setAfficheDate(Date string) {
		this.afficheDate = string;
	}
	
	
}

package com.bx.bean;

public class BxWorker {
	private String fixWorkerId;
	private String workerName;
	private String workerContNum;
	private String workerAddr;
	public String getFixWorkerId() {
		return fixWorkerId;
	}
	public void setFixWorkerId(String fixWorkerId) {
		this.fixWorkerId = fixWorkerId;
	}
	public String getWorkerName() {
		return workerName;
	}
	public void setWorkerName(String workerName) {
		this.workerName = workerName;
	}
	public String getWorkerContNum() {
		return workerContNum;
	}
	public void setWorkerContNum(String workerContNum) {
		this.workerContNum = workerContNum;
	}
	public String getWorkerAddr() {
		return workerAddr;
	}
	public void setWorkerAddr(String workerAddr) {
		this.workerAddr = workerAddr;
	}
	
	
}
